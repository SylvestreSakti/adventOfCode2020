def processPuzzleInput(puzzleInput: str) -> list:
    return puzzleInput.split("\n")


def day3part1(puzzleList):
    pos = 0
    length = len(puzzleList[0])
    trees = 0
    for row in puzzleList:
        if row[pos] == '#':
            trees += 1
        pos = (pos + 3) % length
    return trees

def slope(puzzleList,right,down):
    pos = 0
    length = len(puzzleList[0])
    trees = 0
    for i in range(0,len(puzzleList),down):
        if puzzleList[i][pos] == '#':
            trees += 1
        pos = (pos + right) % length
    return trees

def day3part2(puzzleList):
    return slope(puzzleList,1,1)*slope(puzzleList,3,1)*slope(puzzleList,5,1)*slope(puzzleList,7,1)*slope(puzzleList,1,2)


